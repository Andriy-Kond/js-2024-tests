const x = 10;
const y = 5;

console.log('const x = 10;');
console.log('const y = 5;');

// Додавання
console.log('x + y', x + y); // 15

// Віднімання
console.log('x - y', x - y); // 5

// Множення
console.log('x * y', x * y); // 50

// Ділення
console.log('x / y', x / y); // 2

// Остача від ділення
console.log('x % y (Остача від ділення)', x % y); // 0

console.log('let value = 5;');

// Додавання із заміною (також є для всіх інших операторів)
let value = 5;

// Аналогічно запису value = value + 10;
console.log('value += 10;');
value += 10;
console.log('value', value); // 15
